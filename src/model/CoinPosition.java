package model;

public class CoinPosition {

	/* 金幣可以出現的位置 */

	private int x;
	private int y;

	public CoinPosition(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
